package utils;

/**
 * Hilfe für die Status-Rückgabe bei Fenstern
 * Neue können jederzeit hinzugefügt werden
 * VORSICHT bei Änderungen!
 * 
 * @author mhofmann
 */
public enum WindowStatus {
    /**
     * Nicht definiert
     */
    UNDEFINED,
    /**
     * OK-Button wurde gedrückt
     */
    OK,
    /**
     * Abbrechen-Button wurde gedrückt
     */
    CANCEL,
}
